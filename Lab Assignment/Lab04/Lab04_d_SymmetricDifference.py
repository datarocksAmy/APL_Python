CS = set(["coffee", "code", "programming", "book", "glasses"])
FIN = set(["coffee", "Bloomberg", "Morningstar", "book", "glasses"])

print("< 4-2-b Symmetric Difference of CS & FIN >")
print("- CS  Set :", CS)
print("- FIN Set :", FIN, "\n")
print("--> Symmetric Difference:", CS ^ FIN)
